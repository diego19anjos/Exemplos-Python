# Crie uma lista com as suas frutas favoritas.
# Utilize o operador + para concatenar duas listas de frutas.
# Utilize o operador * para repetir uma lista um determinado número de vezes.

lista_frutas1 = ["maçã", "banana", "laranja"]
lista_frutas2 = ["uva", "morango", "kiwi"]

# Concatena duas listas de frutas
lista_frutas_completa = lista_frutas1 + lista_frutas2

# Repete a lista de frutas 3 vezes
lista_frutas_repetida = lista_frutas1 * 3

# Imprime os resultados
print(f"Lista completa de frutas: {lista_frutas_completa}")
print(f"Lista de frutas repetida 3 vezes: {lista_frutas_repetida}")
