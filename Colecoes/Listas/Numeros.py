# Crie uma lista com números pares de 1 a 10.
# Utilize a função sum() para calcular a soma dos números da lista.
# Utilize a função max() para encontrar o maior número da lista.
# Utilize a função min() para encontrar o menor número da lista.

lista_numeros = [2, 4, 6, 8, 10]

# Calcula a soma dos números da lista
soma_numeros = sum(lista_numeros)

# Encontra o maior número da lista
maior_numero = max(lista_numeros)

# Encontra o menor número da lista
menor_numero = min(lista_numeros)

# Imprime os resultados
print(f"Soma dos números: {soma_numeros}")
print(f"Maior número: {maior_numero}")
print(f"Menor número: {menor_numero}")
